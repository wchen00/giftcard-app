CREATE TABLE users (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
  USERNAME VARCHAR(30),
  PASSWORD  VARCHAR(50),
  PHONE_NUMBER VARCHAR(255),
  CREDIT_CARD_NUMBER VARCHAR(20),
  CVV INTEGER,
  MONTH VARCHAR(2),
  YEAR VARCHAR(5),
  BHN_CREDIT INTEGER
);